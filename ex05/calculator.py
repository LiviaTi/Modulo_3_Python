
def add(num_a:float , num_b:float) -> float:
    result = num_a + num_b
    return (result)

def subtract(num_a:float , num_b:float) -> float:
    result = num_a - num_b
    return (result)

def multiply(num_a:float, num_b:float) -> float:
    result = num_a * num_b
    return (result)

def divide(num_a:float, num_b:float) -> float:
    result = num_a / num_b
    return (result)

